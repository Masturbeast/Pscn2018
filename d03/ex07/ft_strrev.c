char	*ft_strrev(char *str)
{
	int		i;
	int		j;
	char	t;

	i = 0;
	while (str[i] != '\0')
		i++;
	i = i - 1;
	j = 0;
	while (j < i)
	{
		t = str[i];
		str[i] = str[j];
		str[j] = t;
		i = i - 1;
		j = j + 1;
	}
	return (str);
}
