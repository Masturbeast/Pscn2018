void	ft_sort_integer_table(int *tab, int size)
{
	int i;
	int d;

	i = 0;
	while (i <= size - 1)
	{
		if (tab[i] < tab[i + 1])
			i++;
		else
		{
			d = tab[i];
			tab[i] = tab[i + 1];
			tab[i + 1] = d;
			i = 0;
		}
	}
}
