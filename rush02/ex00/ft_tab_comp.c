#include "ft_ft.h"

int		ft_tab_comp(char **tab1, char **tab2, int stat)
{
	int i;
	int j;

	i = 0;
	while (tab1[i])
	{
		j = 0;
		while (tab1[i][j])
		{
			if (tab1[i][j] != tab2[i][j])
				return (0);
			j++;
		}
		i++;
	}
	stat = 1;
	return (1);
}
