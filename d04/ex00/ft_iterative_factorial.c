int		ft_iterative_factorial(int nb)
{
	int a;
	int i;

	a = nb;
	if (nb < 0 || nb > 12)
		return (0);
	if (nb == 0)
		nb = 1;
	nb = 1;
	i = 1;
	while (i < a)
	{
		nb = nb * (i + 1);
		i++;
	}
	return (nb);
}
