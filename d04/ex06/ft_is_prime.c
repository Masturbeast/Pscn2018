int		ft_is_prime(int nb)
{
	int a;

	if (nb < 2 || nb > 2147483647)
		return (0);
	if (nb == 2)
		return (1);
	a = 2;
	while (nb % a != 0)
	{
		a++;
		if (nb % a == 0)
		{
			if (a != nb)
				return (0);
			else
				a++;
		}
		if (a > nb)
			return (1);
	}
	return (0);
}
